
public class MathMethods {

	public int Add(int num1,int num2 ){
		
		return num1+num2;
	}
	
	public int Substract(int num1, int num2){
		
		return num1-num2;
	}
	
	public int Multply(int num1, int num2){
		
		return num1*num2;
	}
	
	public int Division(int num1, int num2){
		
		return num1/num2;
	}

}
